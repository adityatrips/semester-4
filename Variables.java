
public class Variables {
    public static void main(String[] args) {
        // Declaration and initialization of variables
        int age = 25;
        double salary = 50000.50;
        char gender = 'M';
        boolean isEmployed = true;
        String name = "John Doe";

        // Printing the values of variables
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Gender: " + gender);
        System.out.println("Salary: " + salary);
        System.out.println("Employed: " + isEmployed);

        // Modifying the values of variables
        age = 30;
        salary = 60000.75;
        gender = 'F';
        isEmployed = false;
        name = "Jane Smith";

        // Printing the modified values of variables
        System.out.println("Modified Name: " + name);
        System.out.println("Modified Age: " + age);
        System.out.println("Modified Gender: " + gender);
        System.out.println("Modified Salary: " + salary);
        System.out.println("Modified Employed: " + isEmployed);
    }
}
